# Indexing = accessing elements of a sequence using [] 
# [Start:Stop:Step]

phone_number = "123-456-7890"
print(phone_number[0])  
print(phone_number[:3])  
print(phone_number[4:7])
print(phone_number[8:])
print(phone_number[-1])

#Prints every character per step
print(phone_number[::2])

# Reverses the string
print(phone_number[::-1])  